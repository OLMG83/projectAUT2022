package projectJava1HW;

public class Headphones {
    private String brandName;
    private String color;
    private String typeHeadphones;
    private int numberHeadphones;
    private double price;

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getTypeHeadphones() {
        return typeHeadphones;
    }

    public void setTypeHeadphones(String typeHeadphones) {
        this.typeHeadphones = typeHeadphones;
    }

    public int getNumberHeadphones() {
        return numberHeadphones;
    }

    public void setNumberHeadphones(int numberHeadphones) {
        this.numberHeadphones = numberHeadphones;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
