package projectJava1HW;

public class Window {
    private String brandName;
    private String typeWindow;
    private int pieceWindow;
    private double wight;
    private double length;
    private double height;

    public String getBrandName() {
        return brandName;
    }

    public void setBrandName(String brandName) {
        this.brandName = brandName;
    }

    public String getTypeWindow() {
        return typeWindow;
    }

    public void setTypeWindow(String typeWindow) {
        this.typeWindow = typeWindow;
    }

    public int getPieceWindow() {
        return pieceWindow;
    }

    public void setPieceWindow(int pieceWindow) {
        this.pieceWindow = pieceWindow;
    }

    public double getWight() {
        return wight;
    }

    public void setWight(double wight) {
        this.wight = wight;
    }

    public double getLength() {
        return length;
    }

    public void setLength(double length) {
        this.length = length;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }
}
