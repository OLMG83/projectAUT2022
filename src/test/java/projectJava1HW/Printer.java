package projectJava1HW;

public class Printer {
    private String name;
    private double wight;
    private double length;
    private String typePrint;
    private String typeColorPrint;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getWight() {
        return wight;
    }

    public void setWight(double wight) {
        this.wight = wight;
    }

    public double getLength() {
        return length;
    }

    public void setLength(double length) {
        this.length = length;
    }

    public String getTypePrint() {
        return typePrint;
    }

    public void setTypePrint(String typePrint) {
        this.typePrint = typePrint;
    }

    public String getTypeColorPrint() {
        return typeColorPrint;
    }

    public void setTypeColorPrint(String typeColorPrint) {
        this.typeColorPrint = typeColorPrint;
    }
}
