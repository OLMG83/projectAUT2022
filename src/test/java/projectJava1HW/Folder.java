package projectJava1HW;

public class Folder {
    private String color;
    private String typeFolder;
    private int numberCompartments;
    private double length;
    private double width;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getTypeFolder() {
        return typeFolder;
    }

    public void setTypeFolder(String typeFolder) {
        this.typeFolder = typeFolder;
    }

    public int getNumberCompartments() {
        return numberCompartments;
    }

    public void setNumberCompartments(int numberCompartments) {
        this.numberCompartments = numberCompartments;
    }

    public double getLength() {
        return length;
    }

    public void setLength(double length) {
        this.length = length;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }
}
